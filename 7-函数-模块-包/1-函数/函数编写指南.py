#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# 1.应给函数起描述性名称
# 2.函数名只使用小写字母和下划线
# 3.每个函数都应包含简要的阐述其功能的注释，该注释应紧跟在函数定义后面，并采用文档字符串格式

# 4.给形参指定默认值时，等号两边不要有空格，对于函数调用中的关键字实参，也应如此
# 5.PEP 8（https://www.python.org/dev/peps/pep-0008/）建议代码行的长度不要超过79字符，
#   这样只要编辑器窗口适中，就能看到整行代码。如果形参很多，导致函数定义的长度超过了79字符，
#   可回车键另起一行继续添加形参.
# 6.程序或模块包含多个函数，用两个空行将相邻的函数分开，
#   这样更容易知道前一个函数在什么地方结束，下一个函数从什么地方开始
# 7.所有的import语句都应放在文件开头，唯一例外的情形：在文件开头使用了注释来描述整个程序
