"""HTML和HTTP的区别"""


"""在Web应用中，服务器把网页传给浏览器
实际上就是把网页的HTML代码发送给浏览器，让浏览器显示出来
而浏览器和服务器之间的传输协议是HTTP
所以：
    HTML是一种用来定义网页的文本，会HTML，就可以编写网页
    HTTP是在网络上传输HTML的协议，用于浏览器和服务器的通信
"""


