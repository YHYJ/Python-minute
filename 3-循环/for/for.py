# for循环遍历数组输出
for i in range(1, 10):   # 输出从0到9，range(0,10)意思是从0（包括0）开始，到10（不包括10）为止，取其中所有整数！
    print(i)

# for循环的本质是对一个序列中的元素进行递归

# ======一个循环16次的循环，是====== #
# ====== for n range(0,16) ====== #
for n in range(5):
    print(n)
