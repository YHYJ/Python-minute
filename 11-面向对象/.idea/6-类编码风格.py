"""类编码风格"""

"""驼峰法.

【类名】 采用驼峰法，即将类名中的每个单词的首字母都大写，而不使用下划线;
而 【实例名】 和 【模块名】 都采用小写格式，并在单词之间加上下划线.
"""

""" 【文档字符串】 遵循编写函数的文档字符串时的格式约定.

每个模块都应包含一个文档字符串，对其中的类可用于做什么进行描述;
类定义后面也应紧跟一个文档字符串,简要地描述类的功能.
"""

"""可使用空行来组织代码，但不要滥用.

在模块中，可使用两个空行来分隔类;
在类中，可使用一个空行来分隔方法.
"""

"""同时导入标准库中的模块和自建模块时的顺序.

先编写导入标准库模块的import语句，
再添加一个空行，然后导入自建模块.
在包含多条import语句的程序中，这种做法让人更容易明白程序使用的各个模块都来自何方.
"""